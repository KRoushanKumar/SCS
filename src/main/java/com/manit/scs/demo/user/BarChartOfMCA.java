package com.manit.scs.demo.user;

public class BarChartOfMCA {
		private int year;
		private int students;
		
		public BarChartOfMCA()
		{
			
		}
		
		public BarChartOfMCA(int year, int students) {
			super();
			this.year = year;
			this.students = students;
		}
		public int getYear() {
			return year;
		}
		public void setYear(int year) {
			this.year = year;
		}
		public int getStudents() {
			return students;
		}
		public void setStudents(int students) {
			this.students = students;
		}
		
		
}
